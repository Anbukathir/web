const express = require('express');
const router = express.Router();
const userController = require('../controller/user.controller');
const userValidation = require('../validation/user.validation');
const validate = require('../middelware/validate');
const auth = require('../middelware/auth');

router
  .route("/")
  .post(auth(), validate(userValidation.postUserController), userController.postUserController)
  .get(auth('getUsers'), userController.getUsers)
  .get(auth('getUser'), userController.getUser);

router
  .route("/:userId")
  .post(
    auth("postUser"),
    validate(userValidation.postUserController),
    userController.postUserController
  )
  .get(
    auth("getUsers"),
    validate(userValidation.getUser),
    userController.getUser
  )
  .get(
    auth("getUser"),
    validate(userValidation.getUser),
    userController.getUser
  )
  .put(
    auth("updateUser"),
    validate(userValidation.updateUser),
    userController.updateUser

  )

  .delete(
    auth("manageUsers"),
    validate(userValidation.updateUser),
    userController.updateUser
  )
router
  .route("/:userid")
  .get(userController.getUser)
  .put(userController.updateUser)
  .delete(userController.deleteUser);

module.exports = router;

